num = int(input("Digite um número de 1 a 7: "))

if num == 1:
    print("É domingo")
elif num == 2:
    print("É segunda-feira")
elif num == 3:
    print("É terça-feira")
elif num == 4:
    print("É quarta-feira")
elif num == 5:
    print("É quinta-feira")
elif num == 6:
    print("É sexta-feira")
elif num == 7:
    print("É sábado")
else:
    print("Número inválido")